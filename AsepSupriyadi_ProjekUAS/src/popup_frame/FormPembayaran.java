    private void checkTunai() {
        Double totalPembelian = Double.parseDouble(label_totalPembelian.getText());
        Double currentTunai = (Double) txt_tunai.getValue();

        Double kembalian = currentTunai - totalPembelian;

        txt_kembali.setText(kembalian.toString());

        if (currentTunai >= totalPembelian) {
            btn_lunas.setEnabled(true);
        } else if (currentTunai < totalPembelian) {
            btn_lunas.setEnabled(false);
        }
    }

    private void hardReset() {
        txt_tunai.setValue(0D);
        txt_kembali.setText("");
    }

