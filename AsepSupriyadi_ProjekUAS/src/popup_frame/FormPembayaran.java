    private void btn_lunasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_lunasActionPerformed
        // TODO add your handling code here:
        transaksiSaatIni.setTotalDibayarkan((Double) txt_tunai.getValue());
        transaksiSaatIni.setTotalKembalian(Double.parseDouble(txt_kembali.getText()));

        try {
            PenjualanServices.tambahDataTransaksi(transaksiSaatIni);

            hardReset();

            JOptionPane.showMessageDialog(null, "Transaksi selesai", "Apotik Pharmacy - Asep Supriyadi", JOptionPane.INFORMATION_MESSAGE);

            btn_back.setEnabled(false);
            btn_lunas.setEnabled(false);
            btn_cetak.setEnabled(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btn_lunasActionPerformed

    private void btn_cetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cetakActionPerformed
        // TODO add your handling code here:
        try {
            Desktop.getDesktop().browse(new URL("http://localhost/GenerateReportPhamarcy?fk=" + transaksiSaatIni.getKodeTransaksi()).toURI());

            FormPenjualan f = new FormPenjualan();
            f.setVisible(true);
            this.setVisible(false);

        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_btn_cetakActionPerformed

