    private void txt_keywordKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_keywordKeyReleased
        // TODO add your handling code here:
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try {
            List<Pelanggan> result = PelangganServices.searchPelanggan(txt_keyword.getText());

            for (Pelanggan pelanggan : result) {
                // your logic here
                Object[] o = new Object[6];
                o[0] = pelanggan.getId_pelanggan();
                o[1] = pelanggan.getNama_pelanggan();
                o[2] = pelanggan.getEmail();
                o[3] = pelanggan.getNo_telp();
                o[4] = pelanggan.getAlamat();
                o[5] = pelanggan.getTgl_dibuat();
                model.addRow(o);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_txt_keywordKeyReleased

    private void table_pelangganMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_pelangganMouseClicked
        // TODO add your handling code here:
        btn_tambah.setEnabled(false);
        btn_update.setEnabled(true);
        btn_delete.setEnabled(true);

        int i = table_pelanggan.getSelectedRow();
        if (i == -1) {
            return;
        }

        Integer detail_idPelanggan = (Integer) model.getValueAt(i, 0);
        String detail_namaPelanggan = (String) model.getValueAt(i, 1);
        String detail_noTelpPelanggan = (String) model.getValueAt(i, 2);
        String detail_emailPelanggan = (String) model.getValueAt(i, 3);
        String detail_alamatPelanggan = (String) model.getValueAt(i, 4);

        tempIdPelanggan = detail_idPelanggan;
        txt_namaPelanggan.setText(detail_namaPelanggan);
        txt_noTelp.setText(detail_noTelpPelanggan);
        txt_email.setText(detail_emailPelanggan);
        txt_alamat.setText(detail_alamatPelanggan);
    }//GEN-LAST:event_table_pelangganMouseClicked

