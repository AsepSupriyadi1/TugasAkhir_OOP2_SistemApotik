    private void loadData() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();

        btn_update.setEnabled(false);
        btn_delete.setEnabled(false);

        try {
            List<Pelanggan> result = PelangganServices.ambilDataPelanggan();

            for (Pelanggan pelanggan : result) {
                // your logic here
                Object[] o = new Object[6];
                o[0] = pelanggan.getId_pelanggan();
                o[1] = pelanggan.getNama_pelanggan();
                o[2] = pelanggan.getEmail();
                o[3] = pelanggan.getNo_telp();
                o[4] = pelanggan.getAlamat();
                o[5] = pelanggan.getTgl_dibuat();
                model.addRow(o);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void hardReset() {
        txt_namaPelanggan.setText("");
        txt_noTelp.setText("");
        txt_email.setText("");
        txt_alamat.setText("");

        table_pelanggan.clearSelection();

        loadData();

        btn_tambah.setEnabled(true);
        btn_update.setEnabled(false);
        btn_delete.setEnabled(false);

        tempIdPelanggan = null;
    }

    private void TambahAtauUpdateData(EnumRegister tipeRegister) throws Exception {
        String reqNamaPelanggan = txt_namaPelanggan.getText();
        String reqNoTelp = txt_noTelp.getText();
        String reqEmail = txt_email.getText();
        String reqAlamat = txt_alamat.getText();

        System.out.println(reqNamaPelanggan);
        System.out.println(reqNoTelp);
        System.out.println(reqEmail);
        System.out.println(reqAlamat);

        if (reqNamaPelanggan.equals("") || reqNoTelp.equals("")
                || reqEmail.equals("") || reqAlamat.equals("")) {
            throw new CustomException("LENGKAPI DATA !");
        } else {
            try {
                Pelanggan reqPelanggan = new Pelanggan();
                reqPelanggan.setId_pelanggan(tempIdPelanggan);
                reqPelanggan.setNama_pelanggan(reqNamaPelanggan);
                reqPelanggan.setNo_telp(reqNoTelp);
                reqPelanggan.setEmail(reqEmail);
                reqPelanggan.setAlamat(reqAlamat);

                if (tipeRegister.equals(tipeRegister.ADD)) {
                    PelangganServices.tambahPelanggan(reqPelanggan);

                } else if (tipeRegister.equals(tipeRegister.UPDATE)) {
                    PelangganServices.updateDataPelanggan(reqPelanggan);
                }

            } catch (Exception e) {
                System.out.println(e.getMessage());
                throw new Exception(e.getMessage());
            } finally {
                hardReset();
            }
        }
    }

