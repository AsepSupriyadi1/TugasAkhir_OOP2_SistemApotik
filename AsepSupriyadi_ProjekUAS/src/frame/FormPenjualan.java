    private void btn_bayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_bayarActionPerformed
        // TODO add your handling code here:
        Transaksi transaksi = new Transaksi();
        String[] pelanggan = select_pelanggan.getSelectedItem().toString().split(";");
        Integer idPelanggan = Integer.parseInt(pelanggan[1]);

        transaksi.setIdPelanggan(idPelanggan);
        transaksi.setKodeTransaksi(label_kodePenjualan.getText());
        transaksi.setTotalPembelian(Double.parseDouble(label_totalPembelian.getText()));

        FormPembayaran f = new FormPembayaran(transaksi);
        f.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_btn_bayarActionPerformed

