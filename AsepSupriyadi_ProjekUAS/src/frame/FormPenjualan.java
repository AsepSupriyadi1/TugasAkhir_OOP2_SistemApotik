    private void validateItemchanged() {
        try {
            List<ItemPenjualan> result = PenjualanItemServices.ambilDataItemPenjualan(label_kodePenjualan.getText());
            Double totalPembelian = 0.0;

            btn_bayar.setEnabled(false);
            btn_resetData.setEnabled(false);

            if (!result.isEmpty()) {
                btn_bayar.setEnabled(true);
                btn_resetData.setEnabled(true);

                for (ItemPenjualan penjualan : result) {
                    totalPembelian += penjualan.getTotal();
                }
            }

            label_totalPembelian.setText(totalPembelian.toString());
        } catch (Exception e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void loadData() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();

        btn_hapusItem.setEnabled(false);
        btn_ubahItem.setEnabled(false);
        try {
            List<ItemPenjualan> result = PenjualanItemServices.ambilDataItemPenjualan(label_kodePenjualan.getText());

            for (ItemPenjualan itemPenjualan : result) {
                // your logic here
                System.out.println(itemPenjualan.toString());

                Object[] o = new Object[6];
                o[0] = itemPenjualan.getIdEntryTransaksi();
                o[1] = itemPenjualan.getKodeObat();
                o[2] = itemPenjualan.getNamaBarang();
                o[3] = itemPenjualan.getQuantity();
                o[4] = itemPenjualan.getHargaItem();
                o[5] = itemPenjualan.getTotal();

                model.addRow(o);
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void kode() {
        try {
            String result = PenjualanServices.ambilKodeTransaksi();
            label_kodePenjualan.setText(result);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void initiateOpsiPelanggan() {
        try {
            List<Pelanggan> result = PelangganServices.ambilDataPelanggan();

            for (Pelanggan pelanggan : result) {
                // your logic here
                select_pelanggan.addItem(pelanggan.getNama_pelanggan() + ";" + pelanggan.getId_pelanggan());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

