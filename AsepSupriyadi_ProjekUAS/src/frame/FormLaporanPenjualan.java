    private void btn_cetakByPelangganActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cetakByPelangganActionPerformed
        // TODO add your handling code here:
        String[] pelanggan = select_pelanggan.getSelectedItem().toString().split(";");
        String formattedPelanggan = pelanggan[1].replaceAll("\\s+", "");
        Integer idPelanggan = Integer.parseInt(formattedPelanggan);

        try {
            String urlString = "http://localhost/GenerateReportPhamarcy/laporan?pelanggan=" + idPelanggan;
            URI uri = new URL(urlString).toURI();
            Desktop.getDesktop().browse(uri);
        } catch (UnsupportedEncodingException e) {
            System.out.println("Error encoding URL parameter: " + e.getMessage());
        } catch (Exception e) {
            System.out.println("Error opening URL: " + e.getMessage());
        }
    }//GEN-LAST:event_btn_cetakByPelangganActionPerformed

