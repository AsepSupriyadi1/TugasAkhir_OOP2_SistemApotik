    private void initiateSelectPelangganOptions() {
        try {
            List<Pelanggan> listPelanggan = PelangganServices.ambilDataPelanggan();
            for (Pelanggan pelanggan : listPelanggan) {
                select_pelanggan.addItem(pelanggan.getNama_pelanggan() + " ; " + pelanggan.getId_pelanggan());
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void initiateSelectKategoriObatOptions() {
        for (EnumKategoriObat kategoryObat : EnumKategoriObat.values()) {
            select_kategori.addItem(kategoryObat.name());
        }
    }

